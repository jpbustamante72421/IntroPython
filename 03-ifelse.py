'''
Docstring for 03-ifelse
la sentencia if-else es una estructura de control que permite
ejecutar diferentes bloques de codigo en funcion de una condicion.
'''
num1=3
num2=5
#el if solo imprime cuando sea verdadero
if num1>num2:
    print("El numero {} es mayor que el numero {}". format(num1,num2))
#el else es para cuando es falso
else:
    print("El numero {} es menor que el numero {}". format(num1,num2))

